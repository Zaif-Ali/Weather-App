const getinfo = async (event) => {
  // page after alert comes to the initial state so for stoping this we wrote this line
  event.preventDefault();
  // console.log(cityname.value);
  // alert("hello");

  if (cityname.value === "") {
    alert("Please Enter city name for search");
  } else {
    try {
      let url = `https://api.openweathermap.org/data/2.5/weather?q=${cityname.value}&units=metric&appid=83dce64ad8ccc93b4255b9bb7c4f22de`;
      const response = await fetch(url);
      const data = await response.json();
      //    console.log(data);
      const arrData = [data];
      // data putting in the hbs file
      // city name
      city_name.innerHTML = `${arrData[0].name},${arrData[0].sys.country}`;
      // temperature
      const temperature = arrData[0].main.temp;
      temp.innerHTML = Math.round(temperature);
      temp_status.innerHTML = arrData[0].weather[0].main;
      const img_data = arrData[0].weather[0].main;
      if (img_data == "Clouds") {
        img_div.innerHTML = Clouds;
      } else if (img_data == "Clear") {
        img_div.innerHTML = Clear;
      } else if (img_data == "Rain") {
        img_div.innerHTML = Rain;
      } else if (img_data == "Smoke") {
        img_div.innerHTML = Smoke;
      }
    } catch (error) {
      alert("Invalid City Name Please enter valid name for search");
    }
  }
  cityname.value = ""; // clear the search bar for next input
};

